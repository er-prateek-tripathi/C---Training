/*3. Write a program that reads 10 integers and prints the first and the last on one line the second and 
the 9th on the next line the 3rd and the 7th on the next line and so forth.

*/
#include<iostream>
using namespace std;
int main()
{
    int a,b,c,d,e,f,g,h,i,j;
    cout<<"Please enter the numbers : ";
    cin>>a>>b>>c>>d>>e>>f>>g>>h>>i>>j;
    cout<<"Your Numbers are :"<<"\n";
    cout<<a<<"\t"<<j<<"\n";
    cout<<b<<"\t"<<i<<"\n";
    cout<<c<<"\t"<<h<<"\n";
    cout<<d<<"\t"<<g<<"\n";
    cout<<e<<"\t"<<f<<"\n";
    return 0;
}

